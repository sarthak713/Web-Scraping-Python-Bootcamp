# While Loops
    # for loops run a definite number of times, 
    # while loops repeat an indefinite number of times, until a condition is met
    # each time the loop runs, condition is re-evaluated, repeating indefinitely until the condition evaluates to False
    # always make sure that the body of while loop modifies some part of the condition

i=1
while i<=5:
    print(i)
    i+=1